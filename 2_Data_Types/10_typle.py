# Python Data Type - Tuple (Immutable)

# Sample Tuple
my_tuple = (1,2,3,4,5,1,1,1)
print(my_tuple)

output = my_tuple[1]
print(output)

output = my_tuple[-2]
print(output)

# Count method
output = my_tuple.count(1)
print(output)

# Finding Index value of the first occurrence of an item 
output = my_tuple.index(2)
print(output)