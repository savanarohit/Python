# Python Data Type - Number

# Numbers
roll_number = 100
print(roll_number)

# Addition
add_number = 5 + 2
print(add_number)

# Multiplication
multiplication_number = 100 * 2
print(multiplication_number)

# Division
div_number = 100 / 10
print(div_number)

# Square
sequare_number = 2**2
print(sequare_number)

# Modulus
mod_number = 100 % 6
print(mod_number)


# Re-Assign a Variable
my_dog = "tommy"
print(my_dog)
my_cat = my_dog
print(my_cat)


# BODMAS stands for "Brackets, Order, Division and Multiplication, Addition and Subtraction," and it is a rule used in mathematics and programming to determine the sequence of operations when evaluating mathematical expressions.

my_number = (10 + 2) * (2 + 20)
print(my_number)

# To check Variable's Type
print(type(my_number))
print(type(roll_number))

my_float = 1.2
print(type(my_float))

