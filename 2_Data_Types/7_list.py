# Python Data Type - List  (Mutable)

# Sample List
my_list = ["1,'Hello',3,4"]
print(my_list)

my_list = [10,12]
print(my_list)

# Index
final = my_list[0]
print(final)

final2 = my_list[1]
print(final2)

# Append
my_list = [3,4,5,6,3]
my_list.append(7)
print(my_list)

# Count
count_list = my_list.count(3)
print(count_list)

# Length
len(my_list)
print(my_list)

# Pop       (Remove last element of a list)
list_pop = my_list.pop(5)
print(my_list)

# reverse
my_list = [1,2,3,4,'Hello']
my_list.reverse()
print(my_list)

# sort
my_list = [1,2,3,12,4,5,19]
my_list.sort()
print(my_list)